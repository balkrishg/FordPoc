DROP TABLE IF EXISTS INC_PGM_DATA;
CREATE TABLE INC_PGM_DATA
(
   PROGRAM_CODE VARCHAR (10) NOT NULL PRIMARY KEY,
   PROGRAM_NAME VARCHAR (50) NOT NULL,
   DATE_FROM DATE NOT NULL,
   DATE_TO DATE NOT NULL
);
DROP TABLE IF EXISTS INC_PRD_STRUCTURE;
CREATE TABLE INC_PRD_STRUCTURE
(
   INC_STRUCTURE_ID BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
   PROGRAM_CODE VARCHAR (10) NOT NULL,
   PROGRAM_NAME VARCHAR (50) NOT NULL,
   DATE_FROM DATE NOT NULL,
   DATE_TO DATE NOT NULL,
   PRODUCT_TYPE VARCHAR (50) NOT NULL,
   SUB_PRODUCT_TYPE VARCHAR (10) NOT NULL,
   PRODUCT_SALE_TYPE VARCHAR (50) NOT NULL,
   CONTRACT_TYPE VARCHAR (50) NOT NULL,
   RECEPIENT VARCHAR (50) NOT NULL,
   NO_OF_SERVICES INTEGER NOT NULL,
   PERFORMANCE_TARGET VARCHAR (50) NOT NULL,
   INCENTIVES INTEGER NOT NULL
);
DROP TABLE IF EXISTS INC_DLR_PRD_TARGET;
CREATE TABLE INC_DLR_PRD_TARGET
(
   ID BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
   PRODUCT_TYPE VARCHAR (50),
   DEALER_CODE VARCHAR (10),
   DEALER_NAME VARCHAR (100),
   SUB_PRD_TYPE_OSP VARCHAR (10),
   DEALER_TARGET_OSP INTEGER,
   SUB_PRD_TYPE_SSP VARCHAR (10),
   DEALER_TARGET_SSP INTEGER,
   DEALER_TARGET_MONTH VARCHAR (10)
);
DROP TABLE IF EXISTS INC_DLR_CONTRACT_SALES;
CREATE TABLE INC_DLR_CONTRACT_SALES
(
   ID BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
   DEALER_CODE VARCHAR (10),
   DEALER_NAME VARCHAR (100),
   PRODUCT_TYPE VARCHAR (50),
   SUB_PRD_TYPE VARCHAR (10),
   NO_OF_CLAIMS_ALLOWED INTEGER,
   NO_OF_CLAIMS_USED INTEGER,
   PRODUCT_SALE_TYPE VARCHAR (50),
   CONTRACT_TYPE VARCHAR (50),
   CONTRACT_STATUS VARCHAR (5),
   PRODUCT_CODE VARCHAR (50),
   CONTRACT_PLAN_ODOMETER BIGINT,
   CONTRACT_REGISTRATION_DATE DATE,
   CONTRACT_PLAN_TERM INTEGER
);
DROP TABLE IF EXISTS INC_DLR_CONTRACT_SALES_CANCELLATION;
CREATE TABLE INC_DLR_CONTRACT_SALES_CANCELLATION
(
   ID BIGINT NOT NULL AUTO_INCREMENT PRIMARY KEY,
   DEALER_CODE VARCHAR (10),
   DEALER_NAME VARCHAR (100),
   PRODUCT_TYPE VARCHAR (50),
   SUB_PRD_TYPE VARCHAR (10),
   NO_OF_CLAIMS_ALLOWED INTEGER,
   PRODUCT_SALE_TYPE VARCHAR (50),
   CONTRACT_TYPE VARCHAR (50),
   CONTRACT_STATUS VARCHAR (5),
   PRODUCT_CODE VARCHAR (50),
   CONTRACT_PLAN_ODOMETER BIGINT,
   CONTRACT_REGISTRATION_DATE DATE,
   CONTRACT_CANCELLATION_DATE DATE,
   CONTRACT_PLAN_TERM INTEGER
);